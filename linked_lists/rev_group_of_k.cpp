/*
  Reverse a linked list
  The input list will have at least one element  
  Return the node which points to the head of the new LinkedList
  Node is defined as 
    struct node
    {
        int data;
        struct node* next;
    
        node(int x){
            data = x;
            next = NULL;
        }
    
    }*head;
*/

class Solution
{
    public:
    struct node *reverse (struct node *head, int k)
    { 
            // base case: if the list is empty or has less than k nodes, return the head
    if (head == NULL || k <= 1) {
        return head;
    }
    
    // reverse the first k nodes
    node* curr = head;
    node* prev = NULL;
    node* t = NULL;
    int c = 0;
    while (curr && c < k) {
        t = curr->next;
        curr->next = prev;
        prev = curr;
        curr = t;
        c++;
    }
    
    // recursively reverse the next group of k nodes and link the two groups
    if (t) {
        head->next = reverse(t, k);
    }
    
    // return the new head of the reversed list
    return prev;
    }
};


